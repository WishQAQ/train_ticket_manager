<script>
    export default {
        name: "chargeOffOrder",
        components:{
            'Pagination': () => import('@/components/Pagination')
        },
        data(){
            return {
                loading: false,
                order_sn: '',
                order_status: '',
                customer: '',
                issuer: '',
                begin: '',
                end: '',
                client: [],
                dialogTableVisible: false,
                paginationList: {},
                per_page: 10,
                page: '',
                tableData: []
            }
        },
        methods:{

            //移除多选
            toggleSelection(row) {
                this.$refs.multipleTable.toggleRowSelection(row,false);
            },

            //获取客户列表
            getClient(){
                this.$axios.get('/api/user/customer/showAll')
                    .then(res =>{
                        this.client = res.data.result;
                    })
            },

            //获取列表
            getData(){
                let postData = { order_sn : this.order_sn, order_status : this.order_status, customer  : this.customer, issuer  : this.issuer, begin  : this.begin, end   : this.begin };
                this.loading = true;
                this.$axios.post('/api/finance/getInfo/2/'+this.per_page,postData)
                    .then(res =>{
                        this.tableData = res.data.data;
                        this.loading = false;
                        this.paginationList = res.data;
                    })
            },
            //搜索
            search(){
                this.page = '';
                this.getData()
            },
            //修改分页条数
            jumpSize(val){
                this.per_page = val
                this.getData()
            },
            //翻页
            jumpPage(val){
                this.page = val
                this.getData()
            },
        },
        created() {
            this.getData();
            this.getClient();
        }
    }
</script>

